#include "libft.h"

void			*ft_memcpy(void *dest, const void *src, size_t n)
{
	char		*pdest;
	const char	*psrc;

	pdest = dest;
	psrc = src;
	while (n--)
	{
		*pdest = *psrc;
		pdest++;
		psrc++;
	}
	return (dest);
}
